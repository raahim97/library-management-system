    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String sql="select * from Issue where BookID=? ";
        try{
            pst=conn.prepareStatement(sql);
            String s1=jTextField1.getText();
            pst.setString(1, s1);
            rs=pst.executeQuery();
            if(rs.next()){
                jTextField2.setText(rs.getString("ISBN"));
                jTextField3.setText(rs.getString("Name"));
                jTextField4.setText(rs.getString("Edition"));
                jTextField5.setText(rs.getString("Author"));
                jTextField6.setText(rs.getString("Price"));
                jTextField7.setText(rs.getString("Pages"));
                jTextField9.setText(rs.getString("StudentID"));
                jTextField10.setText(rs.getString("StudentName"));
                jTextField11.setText(rs.getString("FatherName"));
                jTextField12.setText(rs.getString("Institute"));
                jTextField13.setText(rs.getString("Branch"));
                jTextField14.setText(rs.getString("Course"));
                jTextField15.setText(rs.getString("Year"));
                jTextField16.setText(rs.getString("Semester"));
                ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).setText(rs.getString("DateOfIssue"));
                ((JTextField)jDateChooser2.getDateEditor().getUiComponent()).setText(rs.getString("ReturnDue"));
                rs.close();
                pst.close();
            }else{
                JOptionPane.showMessageDialog(null,"No Issue Record Found");
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception e){

            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

